package javastart.vo;

public class EmpVo {
	
	private int id;
	private String name;
	private String email;
	private String phone;
	private double salary;
	private int enhance;
	private int tot;
	private int minSal;
	private int maxSal;
	/**
	 * @return the id
	 */
	public int getId() {
		return id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(int id) {
		this.id = id;
	}
	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}
	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}
	/**
	 * @return the email
	 */
	public String getEmail() {
		return email;
	}
	/**
	 * @param email the email to set
	 */
	public void setEmail(String email) {
		this.email = email;
	}
	/**
	 * @return the phone
	 */
	public String getPhone() {
		return phone;
	}
	/**
	 * @param phone the phone to set
	 */
	public void setPhone(String phone) {
		this.phone = phone;
	}
	/**
	 * @return the salary
	 */
	public double getSalary() {
		return salary;
	}
	/**
	 * @param salary the salary to set
	 */
	public void setSalary(double salary) {
		this.salary = salary;
	}
	/**
	 * @return the enhance
	 */
	public int getEnhance() {
		return enhance;
	}
	/**
	 * @param enhance the enhance to set
	 */
	public void setEnhance(int enhance) {
		this.enhance = enhance;
	}
	/**
	 * @return the tot
	 */
	public int getTot() {
		return tot;
	}
	/**
	 * @param tot the tot to set
	 */
	public void setTot(int tot) {
		this.tot = tot;
	}
	/**
	 * @return the minSal
	 */
	public int getMinSal() {
		return minSal;
	}
	/**
	 * @param minSal the minSal to set
	 */
	public void setMinSal(int minSal) {
		this.minSal = minSal;
	}
	/**
	 * @return the maxSal
	 */
	public int getMaxSal() {
		return maxSal;
	}
	/**
	 * @param maxSal the maxSal to set
	 */
	public void setMaxSal(int maxSal) {
		this.maxSal = maxSal;
	}
	
	

}
